<p>3.С помощью цикла for сформируйте строку '-1-2-3-4-5-6-7-8-9-' и запишите ее в переменную $str.</p>

<?php

	$str = '-';
	
	for ($i = 1; $i <= 9; $i++)
		{
		$str .= $i.'-';
		}
	
	echo $str;
?>

