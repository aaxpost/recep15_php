<p>1.С помощью цикла for сформируйте строку '123456789' и запишите ее в переменную $str.</p>

<?php

	$str = '';
	
	for ($i = 1; $i <= 9; $i++)
		{
		$str .= $i;
		}
	
	echo $str;
?>

