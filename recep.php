<p>4.Нарисуйте пирамиду, как показано на рисунке, только у вашей пирамиды должно быть 20 рядов, а не 5:</p>
<p>Старался решить по памяти и сделал на двух циклах</p>

<?php
	
	for ($i = 1; $i <= 20; $i++) {
		$str = '';
