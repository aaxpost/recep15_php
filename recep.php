<p>2.С помощью цикла for сформируйте строку '987654321' и запишите ее в переменную $str.</p>

<?php

	$str = '';
	
	for ($i = 9; $i >= 1; $i--)
		{
		$str .= $i;
		}
	
	echo $str;
?>

